/**
 * Reads Hardhat artifact from repo root and writes web/src/lib/escrow-artifact.ts
 * Run from repo root: npm run sync:abi --prefix web
 * Or from web/: npm run sync:abi
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(webRoot, "..");
const artifactPath = path.join(
  repoRoot,
  "artifacts/contracts/Escrow.sol/Escrow.json"
);
const outPath = path.join(webRoot, "src/lib/escrow-artifact.ts");

if (!fs.existsSync(artifactPath)) {
  console.error(
    "Missing artifact. Run `npx hardhat compile` at the repo root first."
  );
  process.exit(1);
}

const artifact = JSON.parse(fs.readFileSync(artifactPath, "utf8"));
const abiStr = JSON.stringify(artifact.abi, null, 2);
const bytecode = artifact.bytecode;

const header = `// AUTO-GENERATED by npm run sync:abi — do not edit manually.\n`;
const body = `${header}export const escrowAbi = ${abiStr} as const;\n\nexport const escrowBytecode = ${JSON.stringify(bytecode)} as \`0x\${string}\`;\n`;

fs.writeFileSync(outPath, body, "utf8");
console.log("Wrote", outPath);
